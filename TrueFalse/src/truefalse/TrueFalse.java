package truefalse;

/**
 * @author Anna
 * 
 * A program for demonstrating boolean variables
 */
public class TrueFalse
{

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args)
    {
        boolean bool;
        
        bool = true;
        System.out.println(bool);
        
        bool = false;
        System.out.println(false);
        
    }
    
}
